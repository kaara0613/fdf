void	reset_map_index(t_coordinate	*map_size)
{
		map_size->x_i = 0;
		map_size->y_i = 0;
}

ssize_t	int_array_to_string(int *arr)
{
	ssize_t	nums_arr;
	ssize_t	string_len;

	num_arr = 0;
	while(nums_arr <= arr[0])
	{
		num_arr++;
	}
	return (string_len);
}